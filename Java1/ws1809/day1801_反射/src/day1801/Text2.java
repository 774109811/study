package day1801;

import java.lang.reflect.Constructor;
import java.util.Scanner;

public class Text2 {
	public static void main(String[] args) throws Exception {
		System.out.println("输入类名");
		String s = new Scanner(System.in).nextLine();
		//获得它的类名
		Class<?> c = Class.forName(s);
		//有参和无参构造创建对象
		
		System.out.println("\n\n----无参构造――――――");
		f1(c);
		System.out.println("\n\n----有参构造――int参数――――");
		f2(c);
		
	}

	private static void f1(Class<?> c) {
		try {
			//执行无参构造，如果没有无参构造出现异常
			//反射新建无参构造方法
			Object obj = c.newInstance();
			System.out.println(obj);
		} catch (Exception e) {
			System.out.println("无法执行无参构造方法");
		}
		
	}

	private static void f2(Class<?> c) {
	try {
		//获得接受int参数的构造方法，如果不存在出现 异常
		//先获取构造方法
		Constructor<?> t = c.getConstructor(int.class);
		//用构造方法新建实例
		Object obj = t.newInstance(100);
		System.out.println(obj);
	} catch (Exception e) {
		System.out.println("无法执行int构造方法");
	}
	//	java.lang.Integer
	//	java.util.Date
	//	java.util.ArrayList
	//	day1801.Student
	}
}
